export function gql(strings, ...args) {
  let str = "";
  strings.forEach((string, i) => {
    str += string + (args[i] || "");
  });
  return str;
}
export const BlogPartsFragmentDoc = gql`
    fragment BlogParts on Blog {
  __typename
  title
  heroImage
  heroImageCaption
  authors
  date
  category
  tags
  isHeroBanner
  isHotTopic
  isFeatured
  summary
  gallery {
    __typename
    image
    caption
  }
  body
}
    `;
